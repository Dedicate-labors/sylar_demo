//  iomanager.addConditionTimer条件cond参数类型，下面是 超时条件 类型
struct timer_info {
    int cancelled = 0;
};

//  读写函数的统一IO函数模板
template<typename OriginFun, typename... Args>
static ssize_t do_io(int fd, OriginFun fun, const char* hook_fun_name,
        uint32_t event, int timeout_so, Args&&... args) {
    if(!sylar::t_hook_enable) {
        return fun(fd, std::forward<Args>(args)...);
    }

    sylar::FdCtx::ptr ctx = sylar::FdMgr::GetInstance().get(fd);
    if(!ctx) {
        // fd对应的ctx不存在
        return fun(fd, std::forward<Args>(args)...);
    }

    if(ctx->isClose()) {
        errno = EBADF;
        return -1;
    }

    if(!ctx->isSocket() || ctx->getUserNonblock()) {
        return fun(fd, std::forward<Args>(args)...);
    }

    // 下面是打开了的socket文件句柄并且!UserNonblock的逻辑
    uint64_t to = ctx->getTimeout(timeout_so);
    // 超时条件
    std::shared_ptr<timer_info> tinfo(new timer_info);

retry:
    ssize_t n = fun(fd, std::forward<Args>(args)...);
    while(n == -1 && errno == EINTR) {
        // 正常中断情况
        n = fun(fd, std::forward<Args>(args)...);
    }
    if(n == -1 && errno == EAGAIN) {
    // 阻塞状态
        sylar::IOManager* iom = sylar::IOManager::GetThis();
        sylar::Timer::ptr timer;
        std::weak_ptr<timer_info> winfo(tinfo);

        if(to != (uint64_t)-1) {
            // 有超时时间，加入定时任务，超时触发之前加入的任务
            timer = iom->addConditionTimer(to, [winfo, fd, iom, event]() {
                // 在任务中使用winfo条件
                auto t = winfo.lock();
                if(!t || t->cancelled) {
                    return;
                }
                // 超时条件设置为超时
                t->cancelled = ETIMEDOUT;
                // 因为超时唤醒fd上的event状态执行事件，在取消它
                iom->cancelEvent(fd, (sylar::IOManager::Event)(event));
            }, winfo);
        }
        
        // 加入当前协程任务，事件状态event；等待正常唤醒fd上的event状态执行事件
        int rt = iom->addEvent(fd, (sylar::IOManager::Event)(event));
        // SYLAR_UNLIKELY没定义
        // if(SYLAR_UNLIKELY(rt)) {
        if(rt) {
            // 加任务失败
            SYLAR_LOG_ERROR(g_logger) << hook_fun_name << " addEvent("
                << fd << ", " << event << ")";
            if(timer) {
                timer->cancel();
            }
            return -1;
        } else {
            // 加任务成功，当前协程开溜
            sylar::Fiber::YieldToHold();
            // 重新唤醒的两条路径：如果正常唤醒，所以需要取消timer；如果是超时唤醒，取消timer没意义
            if(timer) {
                timer->cancel();
            }
            if(tinfo->cancelled) {
                // 通过超时被唤醒的
                errno = tinfo->cancelled;
                return -1;
            }
            // 任务加入成功且正常唤醒，说明有IO事件，从新开始
            goto retry;
        }
    }
    // 如果成功执行fun就返回
    return n;
}

